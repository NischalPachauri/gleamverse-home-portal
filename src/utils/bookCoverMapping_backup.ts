// Function to get book cover for a given title
export function getBookCover(title: string): string {
  // Try exact match first
  if (bookCoverMapping[title]) {
    return bookCoverMapping[title];
  }
  
  // Try case-insensitive match
  const lowerTitle = title.toLowerCase();
  for (const [key, value] of Object.entries(bookCoverMapping)) {
    if (key.toLowerCase() === lowerTitle) {
      return value;
    }
  }
  
  // Try partial match (first 50 characters)
  const partialTitle = title.substring(0, 50).toLowerCase();
  for (const [key, value] of Object.entries(bookCoverMapping)) {
    if (key.toLowerCase().includes(partialTitle) || partialTitle.includes(key.toLowerCase())) {
      return value;
    }
  }
  
  // Return default cover if no match found
  return "/BookCoversNew/default-book-cover.png";
}