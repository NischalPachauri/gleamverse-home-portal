// Function to get book cover by title
export function getBookCover(title: string): string | null {
  // Try exact match first
  if (bookCoverMap[title]) {
    return bookCoverMap[title];
  }
  
  // Try case-insensitive match
  const lowerTitle = title.toLowerCase();
  for (const [key, value] of Object.entries(bookCoverMap)) {
    if (key.toLowerCase() === lowerTitle) {
      return value;
    }
  }
  
  // Try partial match for common variations
  for (const [key, value] of Object.entries(bookCoverMap)) {
    // Remove special characters and compare
    const cleanKey = key.replace(/[^a-zA-Z0-9\s]/g, '').toLowerCase();
    const cleanTitle = title.replace(/[^a-zA-Z0-9\s]/g, '').toLowerCase();
    
    if (cleanKey === cleanTitle) {
      return value;
    }
    
    // Check if one contains the other (for variations in naming)
    if (cleanKey.includes(cleanTitle) || cleanTitle.includes(cleanKey)) {
      return value;
    }
  }
  
  return null;
}
