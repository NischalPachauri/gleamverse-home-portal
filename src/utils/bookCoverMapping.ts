/**
 * Normalize string for comparison by removing special characters and extra spaces
 */
function normalizeString(str: string): string {
  if (!str) return '';
  
  return str
    .toLowerCase()
    .replace(/[^\w\s]/g, ' ') // Replace special chars with space
    .replace(/\s+/g, ' ')      // Replace multiple spaces with single space
    .trim();
}

/**
 * Extract potential book title from PDF filename
 */
function extractTitleFromPdfPath(pdfPath: string): string {
  if (!pdfPath) return '';
  
  // Get filename without extension
  const filename = pdfPath.split('/').pop()?.replace('.pdf', '') || '';
  
  // Convert underscores and hyphens to spaces
  return filename
    .replace(/[-_]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

/**
 * Try to find a matching cover image by checking various patterns
 */
function findMatchingCover(title: string, pdfPath?: string): string | undefined {
  if (!title) return undefined;
  
  const normalizedTitle = normalizeString(title);
  const keys = Object.keys(bookCoverMap);
  
  // Strategy 1: Exact match
  if (bookCoverMap[title]) {
    return bookCoverMap[title];
  }
  
  // Strategy 2: Case-insensitive exact match
  const exactMatch = keys.find(key => key.toLowerCase() === title.toLowerCase());
  if (exactMatch) {
    return bookCoverMap[exactMatch];
  }
  
  // Strategy 3: Normalized match (ignoring special characters)
  const normalizedMatch = keys.find(key => normalizeString(key) === normalizedTitle);
  if (normalizedMatch) {
    return bookCoverMap[normalizedMatch];
  }
  
  // Strategy 4: PDF filename exact match (if PDF path is provided)
  if (pdfPath) {
    const pdfFilename = pdfPath.split('/').pop()?.replace('.pdf', '') || '';
    const exactPdfMatch = keys.find(key => key === pdfFilename);
    if (exactPdfMatch) {
      return bookCoverMap[exactPdfMatch];
    }
  }
  
  // Strategy 5: Partial match - key contains title or vice versa
  const partialMatch = keys.find(key => {
    const normalizedKey = normalizeString(key);
    return normalizedKey.includes(normalizedTitle) || normalizedTitle.includes(normalizedKey);
  });
  if (partialMatch) {
    return bookCoverMap[partialMatch];
  }
  
  // Strategy 6: Try matching with PDF filename
  if (pdfPath) {
    const pdfTitle = extractTitleFromPdfPath(pdfPath);
    const normalizedPdfTitle = normalizeString(pdfTitle);
    
    const pdfMatch = keys.find(key => {
      const normalizedKey = normalizeString(key);
      return normalizedKey.includes(normalizedPdfTitle) || normalizedPdfTitle.includes(normalizedKey);
    });
    
    if (pdfMatch) {
      return bookCoverMap[pdfMatch];
    }
  }
  
  // Strategy 7: Word-based fuzzy matching (check if significant words match)
  const titleWords = normalizedTitle.split(' ').filter(w => w.length > 2); // Reduced from 3 to 2 to catch more matches
  if (titleWords.length > 0) {
    const fuzzyMatches = keys.map(key => {
      const keyWords = normalizeString(key).split(' ').filter(w => w.length > 2);
      const matchCount = titleWords.filter(word => 
        keyWords.some(keyWord => keyWord.includes(word) || word.includes(keyWord))
      ).length;
      
      const matchRatio = titleWords.length > 0 ? matchCount / titleWords.length : 0;
      return { key, matchRatio };
    });
    
    // Sort by match ratio and get the best match
    fuzzyMatches.sort((a, b) => b.matchRatio - a.matchRatio);
    const bestMatch = fuzzyMatches[0];
    
    // If more than 40% of significant words match, consider it a match (reduced from 50%)
    if (bestMatch && bestMatch.matchRatio > 0.4) {
      return bookCoverMap[bestMatch.key];
    }
  }
  
  return undefined;
}

/**
 * Get book cover image path from title
 * @param title Book title
 * @param pdfPath Optional PDF path for additional matching
 * @returns Path to book cover image or undefined if not found
 */
export function getBookCover(title: string, pdfPath?: string): string | undefined {
  if (!title) return undefined;
  
  // Try to find a matching cover
  const coverPath = findMatchingCover(title, pdfPath);
  
  // If found, return the full path
  if (coverPath) {
    // Ensure the path starts with a slash for proper URL resolution
    return coverPath.startsWith('/') ? coverPath : `/${coverPath}`;
  }
  
  return undefined;
}

/**
 * Error handler for image loading
 * @param event Error event
 * @param fallbackSrc Fallback image source
 */
export function handleImageError(
  event: React.SyntheticEvent<HTMLImageElement, Event>, 
  fallbackSrc: string = '/placeholder.svg'
): void {
  const img = event.currentTarget;
  if (img.src !== fallbackSrc && !img.src.includes('placeholder')) {
    console.warn(`Failed to load image: ${img.src}`);
    img.src = fallbackSrc;
  }
}

